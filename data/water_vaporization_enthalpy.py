class Fluid:

    def __init__(self, h_vap, t):
        self.h_vap = h_vap
        self.t = t
        self.h_vap_fit = np.polyfit(self.t, self.h_vap, 10)

    def calc_h_vap(self, t_in):
        return np.polyval(self.h_vap_fit, t_in)


